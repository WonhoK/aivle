### HTML ###
# - 웹문서를 작성하는 언어
# - 구성요소
#   Document : 한 페이지를 나타내는 코드
#   Element : 하나의 레이아웃 (Element가 모여서 Document를 만듬
#   Tag : Element의 종류를 정의 (시작 태그 + 끝 태그 = Element)
#   Attribute : 속성값 (시작태그에서 태그의 기능 정의)
#       * id : element를 지정하는 페이지 내에서 유일한 값
#       * class : element를 지정하는 값으로 페이지 내에서 여러개 사용 가능
#       * attr : id, class를 제외한 나머지 속성값
#   Text : 시작 태그와 끝 태그 사이의 문자열
# - 엘리먼트는 계층적 구조를 갖는다

### HTML 태그 종류 ###
# p : 한 줄의 문자열 출력
# span : 한 블럭의 문자열 출력
# ul, li : 리스트 문자열 출력
# a : 링크를 나타내는 태그 (href)
# img : 이미지를 출력하는 태그
# iframe : 외부 URL링크에 해당하는 웹 페이지를 출력
# div : 레이아웃의 영역을 나타내는 태그
# table : 행열 데이터를 출력

